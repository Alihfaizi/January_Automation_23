package Day1_02252023;

public class T4_Concatenation {
    public static void main(String[] args) {
        String message, numberString;
        int number, number2;

        //declare string date type
        message = "Welcome to automation class";
        numberString = "1";
        //declare integer data type
        number = 1;
        number2 = 200;

        //I want to print message and int number variable on one print statement by using concatenation
        System.out.println("Result is " + message + " " + numberString + " " + number + " " + number2);
    }//end of class
}